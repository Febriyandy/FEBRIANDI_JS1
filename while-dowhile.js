// Contoh program JavaScript menggunakan pernyataan while 

var jamKerja = 0;

while (jamKerja < 5) {
    console.log("Pekerja sedang bekerja selama " + jamKerja + " jam.");
    jamKerja++;
}

console.log("Pekerja telah selesai bekerja.");


// Contoh program JavaScript dengan pernyataan do...while

var j = 4;

do {
    console.log(j);
    j++; 
} while (j <= 10);
